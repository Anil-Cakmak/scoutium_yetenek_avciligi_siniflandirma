def high_correlated_cols(dataframe, corr_th=0.90, plot=False):
    corr = dataframe.corr()
    cor_matrix = corr.abs()
    upper_triangle_matrix = cor_matrix.where(np.triu(np.ones(cor_matrix.shape), k=1).astype(bool))
    drop_list = [col for col in upper_triangle_matrix.columns if any(upper_triangle_matrix[col] > corr_th)]
    if plot:
        sns.set(rc={'figure.figsize': (15, 15)})
        sns.heatmap(corr, cmap="RdBu")
        plt.show(block=True)
    return drop_list


def quick_missing_imp(data, num_method="median", cat_length=20, target="SalePrice"):
    variables_with_na = [col for col in data.columns if data[col].isnull().sum() > 0]
    # Eksik değere sahip olan değişkenler listelenir.

    temp_target = data[target]

    print("# BEFORE")
    print(data[variables_with_na].isnull().sum(), "\n\n")  # Uygulama öncesi değişkenlerin eksik değerlerinin sayısı.

    # değişken object ve sınıf sayısı cat_length'e eşit veya altındaysa boş değerleri mode ile doldur
    data = data.apply(lambda x: x.fillna(x.mode()[0]) if (x.dtype == "O" and len(x.unique()) <= cat_length) else x,
                      axis=0)

    # num_method mean ise tipi object olmayan değişkenlerin boş değerleri ortalama ile dolduruluyor
    if num_method == "mean":
        data = data.apply(lambda x: x.fillna(x.mean()) if x.dtype != "O" else x, axis=0)
    # num_method median ise tipi object olmayan değişkenlerin boş değerleri medyan ile dolduruluyor
    elif num_method == "median":
        data = data.apply(lambda x: x.fillna(x.median()) if x.dtype != "O" else x, axis=0)

    data[target] = temp_target

    print("# AFTER \n Imputation method is 'MODE' for categorical variables!")
    print(" Imputation method is '" + num_method.upper() + "' for numeric variables! \n")
    print(data[variables_with_na].isnull().sum(), "\n\n")

    return data


def voting_importance(voting_model, features, weights):
    """ Function to compute feature importance of Voting Regressor/ Classifier"""

    feature_importance = dict()
    for est in voting_model.estimators_:
        feature_importance[str(est)] = MinMaxScaler().fit_transform(est.feature_importances_.reshape(-1, 1)).reshape(
            len(features.columns))

    fe_scores = [0] * len(list(feature_importance.values())[0])
    for idx, imp_score in enumerate(feature_importance.values()):
        imp_score_with_weight = imp_score * weights[idx]
        fe_scores = list(np.add(fe_scores, list(imp_score_with_weight)))
    return pd.DataFrame({'Feature': features.columns, 'Value': fe_scores})


def plot_voting_importance(model, features, weights, num=10, save=False):
    feature_imp = voting_importance(model, features, weights)
    plt.figure(figsize=(10, 10))
    sns.set(font_scale=1)
    sns.barplot(x="Value", y="Feature", data=feature_imp.sort_values(by="Value",
                                                                     ascending=False)[0:num])
    plt.title('Features')
    plt.tight_layout()
    plt.show(block=True)
    if save:
        plt.savefig('feature_importances.png')


def voting_regressor(best_models, x, y, weight_list=None):
    print("Voting Regressor...")
    voting_reg = VotingRegressor(estimators=best_models, weights=weight_list).fit(x, y)
    cv_results = cross_validate(voting_reg, x, y, cv=5, scoring="neg_root_mean_squared_error")
    print(f"rmse: {-cv_results['test_score'].mean()}")
    return voting_reg


def base_models(models, x, y, scoring="neg_root_mean_squared_error"):
    print("Base Models....")
    for name, model in models:
        cv_results = cross_validate(model, x, y, cv=3, scoring=scoring)
        print(f"{scoring[4:]}: {round(-cv_results['test_score'].mean(), 4)} ({name}) ")


def val_curve_params(model, x, y, param_name, param_range, scoring="roc_auc", cv=10):
    train_score, test_score = validation_curve(
        model, X=x, y=y, param_name=param_name, param_range=param_range, scoring=scoring, cv=cv)

    mean_train_score = np.mean(train_score, axis=1)
    mean_test_score = np.mean(test_score, axis=1)

    plt.plot(param_range, mean_train_score,
             label="Training Score", color='b')

    plt.plot(param_range, mean_test_score,
             label="Validation Score", color='g')

    plt.title(f"Validation Curve for {type(model).__name__}")
    plt.xlabel(f"Number of {param_name}")
    plt.ylabel(f"{scoring}")
    plt.tight_layout()
    plt.legend(loc='best')
    plt.show(block=True)
